"use strict";
String.prototype.toAlternatingCase = function () {
  let result = [];
  let array = this.split("");
  for (let i = 0; i < array.length; i++) {
    if (array[i].match(/[A-Z]/)) {
      result.push(array[i].toLowerCase());
    } else {
      result.push(array[i].toUpperCase());
    }
  }
  return result.join("");
};

